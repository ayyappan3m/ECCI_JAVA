class IntegralLiteral
{
	public static void main(String args[])
	{
		int dec = 11;//decimal
		int oct = 017;//octal
		int hex = 0X1E;//hexa decimal
		int bin = 0b11;//binary
		System.out.println("dec = 11 == "+dec+"\n"+"oct = 017 == "+oct+"\n"+"hex = 0X1E == "+hex+"\n"+"bin = 0b11 == "+bin);

	}  


}
